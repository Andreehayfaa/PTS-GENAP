def perpangkatan(bilangan, pangkat):
    return bilangan ** pangkat

print(perpangkatan(2, 3))
print(perpangkatan(5, 2))

#Line 1 = Membuat fungsi yang menerima dua parameter.
#Line 2 = Menghitung hasil perpangkatan.
#Line 4 = Memanggil fungsi dengan perpangkatan(2, 3) adalah 2³ = 8
#Line 5 = Memanggil fungsi dengan perpangkatan(5, 2) adalah 5² = 25